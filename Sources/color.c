#include "rtv1.h"

int				add_color(int c1, int c2)
{
	int	r;
	int	g;
	int	b;

	b = ft_min(((c1 & 0x000000FF) >> 0) + ((c2 & 0x000000FF) >> 0), 255);
	g = ft_min(((c1 & 0x0000FF00) >> 8) + ((c2 & 0x0000FF00) >> 8), 255);
	r = ft_min(((c1 & 0x00FF0000) >> 16) + ((c2 & 0x00FF0000) >> 16), 255);
	return ((r << 16) + (g << 8) + b);
}
